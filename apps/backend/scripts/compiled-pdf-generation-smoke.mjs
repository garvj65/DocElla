import { mkdtemp, rm } from "node:fs/promises";
import os from "node:os";
import path from "node:path";
import { fileURLToPath } from "node:url";

import { getDocumentDefinition } from "@docella/schemas";
import { PDFDocument } from "pdf-lib";

const backendRoot = path.resolve(fileURLToPath(new URL("../", import.meta.url)));
const tempDir = await mkdtemp(path.join(os.tmpdir(), "docella-pdf-smoke-"));

try {
  const { createPdfGenerationService } =
    await import("../dist/pdf-generation/pdf-generation-service.js");
  const { createFilePdfTemplateRepository } =
    await import("../dist/pdf-generation/pdf-template-repository.js");

  const documentDefinition = getDocumentDefinition("job-application");
  if (documentDefinition === undefined) {
    throw new Error("Missing job-application definition.");
  }
  const template = documentDefinition.templates.find(
    (candidate) => candidate.id === "job-application-default",
  );
  if (template === undefined) {
    throw new Error("Missing job-application-default template.");
  }

  const service = createPdfGenerationService(
    createFilePdfTemplateRepository(new URL("../assets/", import.meta.url)),
  );
  const values = {
    additionalNotes: null,
    address: "123 Example Street",
    availableStartDate: null,
    currentEmployer: null,
    currentJobTitle: null,
    email: "alex@example.test",
    fullName: "Alex Morgan",
    highestEducation: null,
    phone: "+1 555 010 2200",
    positionAppliedFor: "Product Analyst",
    salaryExpectation: 75000,
    yearsOfExperience: 5,
  };

  const editable = await service.generate({
    documentDefinition,
    flatten: false,
    template,
    values,
  });
  if (!Buffer.from(editable.bytes.subarray(0, 5)).equals(Buffer.from("%PDF-"))) {
    throw new Error("Editable generated bytes do not start with %PDF-.");
  }

  const editableDocument = await PDFDocument.load(editable.bytes);
  const editableForm = editableDocument.getForm();
  if (editableForm.getTextField("job.full_name").getText() !== values.fullName) {
    throw new Error("Editable generated PDF did not retain representative field values.");
  }

  const flattened = await service.generate({
    documentDefinition,
    flatten: true,
    template,
    values,
  });
  const flattenedDocument = await PDFDocument.load(flattened.bytes);
  if (flattenedDocument.getForm().getFields().length !== 0) {
    throw new Error("Flattened generated PDF still has editable fields.");
  }

  void backendRoot;
  void tempDir;
} finally {
  await rm(tempDir, { force: true, recursive: true });
}
